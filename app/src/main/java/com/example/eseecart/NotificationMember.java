package com.example.eseecart;

public class NotificationMember {

    String id;
    String message;
    String idpay;
    String date;
    String time;
    String furid;

    public NotificationMember(){

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getIdpay() {
        return idpay;
    }

    public void setIdpay(String idpay) {
        this.idpay = idpay;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
    public String getFurid() {
        return furid;
    }

    public void setFurid(String furid) {
        this.furid = furid;
    }


}
