package com.example.eseecart;

public class feedbackMember {

    String id;
    String userid;
    String message;
    String furid;

    public feedbackMember(){

    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserid() {
        return userid;
    }

    public void setUserid(String userid) {
        this.userid = userid;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public String getFurid() {
        return furid;
    }

    public void setFurid(String furid) {
        this.furid = furid;
    }

}
